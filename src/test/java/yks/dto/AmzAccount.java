package yks.dto;

public class AmzAccount {
	
	private String a_site;
	private String b_account_name;
	private String c_account_mail;
	private String d_ip;
	private String e_principal;
	private String f_account_password;
	
	public String getA_site() {
		return a_site;
	}
	public void setA_site(String a_site) {
		this.a_site = a_site;
	}
	public String getB_account_name() {
		return b_account_name;
	}
	public void setB_account_name(String b_account_name) {
		this.b_account_name = b_account_name;
	}
	public String getC_account_mail() {
		return c_account_mail;
	}
	public void setC_account_mail(String c_account_mail) {
		this.c_account_mail = c_account_mail;
	}
	public String getD_ip() {
		return d_ip;
	}
	public void setD_ip(String d_ip) {
		this.d_ip = d_ip;
	}
	public String getE_principal() {
		return e_principal;
	}
	public void setE_principal(String e_principal) {
		this.e_principal = e_principal;
	}
	public String getF_account_password() {
		return f_account_password;
	}
	public void setF_account_password(String f_account_password) {
		this.f_account_password = f_account_password;
	}
	

}
